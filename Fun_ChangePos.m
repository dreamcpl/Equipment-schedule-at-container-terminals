function Object=Fun_ChangePos(Object,Pos1,Pos2)
%%%%%%%%%%%%%%%%%%模块整体思路%%%%%%%%%%%%%%%%%%
%将一维数组Object中，第Pos1个位置的数和第Pos2个位置的数进行交换。
%%%%%%%%%%%%%%%%%%%%注意事项%%%%%%%%%%%%%%%%%%%%
%Object是一维数组
%%%%%%%%%%%%%各步骤作用及其注意事项%%%%%%%%%%%%%
%1.摘出两个位置的值。
%2.将Pos2位置的值赋予Pos1位置。
%3.将Pos1位置的值赋予Pos2位置。
%4.结果存储。
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
val1=Object(1,Pos1);
val2=Object(1,Pos2);
Object(1,Pos1)=val2;
Object(1,Pos2)=val1;
